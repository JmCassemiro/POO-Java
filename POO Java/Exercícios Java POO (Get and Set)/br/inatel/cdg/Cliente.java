package br.inatel.cdg;

public class Cliente {

    // Atributos

    private String nome;

    private long cpf;

    // Setando e Getando valores

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public long getCpf() {
        return cpf;
    }

    public void setCpf(long cpf) {
        this.cpf = cpf;
    }

}
